public class AtoZ {
    public static void main(String[] args){
        char c1='A';
        while(c1<='E'){
            System.out.print(c1);
            System.out.print(' ');
            c1++;
        }
        System.out.println(' ');
        while(c1<='J') {
            System.out.print(c1);
            System.out.print(' ');
            c1++;
        }
        System.out.println(' ');
        while(c1<='O') {
            System.out.print(c1);
            System.out.print(' ');
            c1++;
        }
        System.out.println(' ');
        while(c1<='T') {
            System.out.print(c1);
            System.out.print(' ');
            c1++;
        }
        System.out.println(' ');
        while(c1<='Y') {
            System.out.print(c1);
            System.out.print(' ');
            c1++;
        }
        System.out.println(' ');
        System.out.print(c1);
    }
}
